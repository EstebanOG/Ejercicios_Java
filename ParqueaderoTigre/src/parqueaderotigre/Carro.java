public class Carro {
    String placa;
    String marca;
    String color;
    int estadodecolor;
    String cedula;

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

 

    public int getEstadodecolor() {
        return estadodecolor;
    }

    public void setEstadodecolor(int estadodecolor) {
        this.estadodecolor = estadodecolor;
    }
    
    
}
